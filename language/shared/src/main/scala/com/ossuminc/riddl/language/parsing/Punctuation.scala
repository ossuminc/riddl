package com.ossuminc.riddl.language.parsing

object Punctuation {
  final val asterisk = "*"
  final val atSign = "@"
  final val comma = ","
  final val colon = ":"
  final val curlyOpen = "{"
  final val curlyClose = "}"
  final val dot = "."
  final val equalsSign = "="
  final val plus = "+"
  final val question = "?"
  final val quote = "\""
  final val roundOpen = "("
  final val roundClose = ")"
  final val squareOpen = "["
  final val squareClose = "]"
  final val undefinedMark = "???"
  final val verticalBar = "|"
}
